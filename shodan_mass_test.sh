# 统计变量
total_tested=0
detected_count=0
declare -A component_detected_count
declare -A detected_ips
declare -A ip_components

# 结果文件
RESULTS_FILE="shodan_mass_test_results_$(date +%Y%m%d_%H%M%S).json"
echo "[]" > "$RESULTS_FILE"

# 测试每个组件类型
for component_type in "${!COMPONENT_SEARCH[@]}"; do
    echo "============================================"
    echo "测试组件类型: $component_type"
    echo "============================================"
    
    # 获取该类型的IP列表
    ips="${TEST_IPS[$component_type]}"
    
    # 每种类型查询间隔60秒
    if [ $total_tested -gt 0 ]; then
        echo "等待60秒（频率控制）..."
        sleep 60
    fi
    
    count=0
    while IFS= read -r target; do
        [ -z "$$target" ] && continue
        
        ((total_tested++))
        ((count++))
        
        echo "[$count/100] 检测: $target (搜索类型: $component_type)"
        
        # 运行检测
        result=$(timeout 15 ./llm-detector -t "$target" --timeout 10s --json 2>/dev/null)
        
        if [ -n "$result" ]; then
            # 检查是否检测到任何组件
            available_count=$(echo "$result" | grep -o '"available": true' | wc -l)
            
            if [ "$available_count" -gt 0 ]; then
                # 提取检测到的组件类型
                detected_types=$(echo "$result" | grep -B2 '"available": true' | grep '"type"' | sed 's/.*"type": "\([^"]*\)".*/\1/' | tr '\n' ', ')
                detected_types=${detected_types%, }
                
                # 去重检查
                ip_only=$(echo "$target" | cut -d: -f1)
                if [ -z "${detected_ips[$ip_only]}" ]; then
                    detected_ips[$ip_only]=1
                    ip_components[$ip_only]="$detected_types"
                    ((detected_count++))
                    
                    # 统计各组件出现次数
                    for comp in $(echo "$detected_types" | tr ',' ' '); do
                        comp=$(echo "$comp" | tr -d ' ')
                        if [ -n "$comp" ]; then
                            component_detected_count[$comp]=$((${component_detected_count[$comp]:-0} + 1))
                        fi
                    done
                    
                    echo "    ✅ 检测到: $detected_types"
                    
                    # 保存结果
                    echo "$result" >> "$RESULTS_FILE"
                else
                    echo "    ⚠️  IP已存在，跳过"
                fi
            else
                echo "    ❌ 未检测到组件"
            fi
        else
            echo "    ❌ 超时/无响应"
        fi
        
        # 每个IP检测间隔1秒
        sleep 1
        
        # 每10个IP暂停一下，避免频率过高
        if [ $((count % 10)) -eq 0 ]; then
            echo "    (已检测 $count 个IP，暂停5秒...)"
            sleep 5
        fi
        
    done <<< "$ips"
    
    echo ""
done

# 记录结束时间
end_time=$(date +%s)
elapsed=$((end_time - start_time))
hours=$((elapsed / 3600))
minutes=$(((elapsed % 3600) / 60))

# 计算成功率
if [ $total_tested -gt 0 ]; then
    success_rate=$(echo "scale=2; $detected_count * 100 / $total_tested" | bc 2>/dev/null || echo "0")
else
    success_rate=0
fi

# 生成报告
echo ""
