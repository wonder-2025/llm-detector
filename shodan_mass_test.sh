echo "- 检测成功率: $success_rate%"
echo "- 总耗时: ${hours}小时${minutes}分钟"
echo ""
echo "## 检测到的组件统计"
echo ""
echo "| 组件类型 | 出现次数 | 占比 |"
echo "|----------|----------|------|"
for comp in "${!component_detected_count[@]}"; do
    count=${component_detected_count[$comp]}
    percentage=$(echo "scale=2; $count * 100 / $detected_count" | bc 2>/dev/null || echo "0")
    echo "| $comp | $count | $percentage% |"
done
echo ""
echo "## 检测成功的IP列表"
echo ""
echo "| IP | 端口 | 搜索类型 | 实际检测到的组件 |"
echo "|----|------|----------|------------------|"
for ip in "${!detected_ips[@]}"; do
    components="${ip_components[$ip]}"
    # 提取端口（从第一个匹配的target中获取）
    port="unknown"
    for target in "${!COMPONENT_SEARCH[@]}"; do
        if echo "${TEST_IPS[$target]}" | grep -q "$ip"; then
            port=$(echo "${TEST_IPS[$target]}" | grep "$ip" | head -1 | cut -d: -f2)
            search_type="$target"
            break
        fi
    done
    echo "| $ip | $port | $search_type | $components |"
done
echo ""
echo "## 详细结果文件"
echo "- 结果保存至: $RESULTS_FILE"
echo ""
echo "============================================"
